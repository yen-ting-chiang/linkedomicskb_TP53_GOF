# For each unique AAchange, determine GOF/LOF/DN status
# by aggregating across all entries (multiple experiments per mutation)

func_annotation <- func_db %>%
  # Filter to single amino acid changes (not compound mutations with "/")
  filter(!grepl("/", AAchange),
         AAchange != "",
         !is.na(AAchange)) %>%
  group_by(AAchange) %>%
  summarise(
    # GOF: any entry with Gain_of_Function that is non-empty, non-NA,
    # and does NOT start with "No"
    has_GOF = any(
      !is.na(Gain_of_Function) &
      Gain_of_Function != "" &
      Gain_of_Function != "NA" &
      !grepl("^No\\b", Gain_of_Function)
    ),
    # LOF: any entry with Loss_of_Function that is non-empty, non-NA
    has_LOF = any(
      !is.na(Loss_of_Function) &
      Loss_of_Function != "" &
      Loss_of_Function != "NA"
    ),
    # DN: any entry with Dominant_Negative_Activity starting with "Yes"
    has_DN = any(
      !is.na(Dominant_Negative_Activity) &
      grepl("^Yes", Dominant_Negative_Activity)
    ),
    .groups = "drop"
  ) %>%
  mutate(
    # Apply classification rules per paper:
    # If both GOF and LOF -> classify as GOF
    GOF = ifelse(has_GOF, 1L, 0L),
    LOF = ifelse(has_LOF & !has_GOF, 1L, 0L),
    DN  = ifelse(has_DN, 1L, 0L),
    non_DN = ifelse(has_DN, 0L, 1L)
  )

cat("GOF/LOF/DN annotation summary from FunctionDownload_r21.csv:\n")
