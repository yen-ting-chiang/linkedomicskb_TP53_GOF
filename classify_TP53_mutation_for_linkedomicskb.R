# Section 4: Main Processing Loop
# ==============================================================================

all_results <- list()

for (i in seq_len(nrow(datasets))) {
  ds_folder <- datasets$folder[i]
  ds_cancer <- datasets$cancer_type[i]

  cat("Processing dataset:", ds_folder, "(", ds_cancer, ")\n")

  # --- Read mutation file ---
  mut_file <- file.path(base_path, ds_folder, paste0(ds_cancer, "_somatic_mutation.maf"))
  
  # Read mutation file, skipping comment lines automatically
  mut_df <- read.delim(mut_file, comment.char = "#", stringsAsFactors = FALSE, sep = "\t")
  
  # Extract all unique samples from the mutation file
  all_samples <- unique(mut_df$Tumor_Sample_Barcode)
  cat("  Total samples (from MAF):", length(all_samples), "\n")

  # --- Filter for TP53 mutations ---
  tp53_muts <- mut_df %>%
    filter(Hugo_Symbol == "TP53") %>%
    select(Tumor_Sample_Barcode, Variant_Classification, aaChange) %>%
    distinct()

  cat("  TP53 mutation entries:", nrow(tp53_muts), "\n")

  # --- Classify each sample ---
  sample_results <- data.frame(
    sample_id = character(),
    cancer_type = character(),
    protein_change = character(),
    variant_classification = character(),
    wt = integer(),
    mt = integer(),
    LOF = integer(),
    GOF = integer(),
    DN = integer(),
    non_DN = integer(),
    hotspot = integer(),
    category = character(),
    stringsAsFactors = FALSE
  )

  for (sample in all_samples) {
    # Get TP53 mutations for this sample
    sample_tp53 <- tp53_muts %>%
      filter(Tumor_Sample_Barcode == sample)

    if (nrow(sample_tp53) == 0) {
      # No TP53 mutations -> TP53wt
      sample_results <- rbind(sample_results, data.frame(
        sample_id = sample,
        cancer_type = ds_cancer,
        protein_change = "[Not Available]",
        variant_classification = NA_character_,
        wt = 1L, mt = 0L,
        LOF = 0L, GOF = 0L, DN = 0L, non_DN = 0L,
        hotspot = 0L,
        category = "TP53wt",
        stringsAsFactors = FALSE
      ))
      next
    }

    # Check for TP53mut-type variants
    mut_type_rows <- sample_tp53 %>%
      filter(Variant_Classification %in% tp53mut_variants)

    if (nrow(mut_type_rows) == 0) {
      # Has TP53 entries but only synonymous/intronic/UTR -> TP53wt
      # Use the first protein change for reference
      pc <- sample_tp53$aaChange[1]
      if (is.na(pc) || pc == "" || pc == ".") pc <- "[Not Available]"
      sample_results <- rbind(sample_results, data.frame(
        sample_id = sample,
        cancer_type = ds_cancer,
        protein_change = pc,
        variant_classification = sample_tp53$Variant_Classification[1],
        wt = 1L, mt = 0L,
        LOF = 0L, GOF = 0L, DN = 0L, non_DN = 0L,
        hotspot = 0L,
        category = "TP53wt",
        stringsAsFactors = FALSE
      ))
      next
    }

    # TP53mut: classify each mutation and select the most impactful
    classified_muts <- list()
    for (j in seq_len(nrow(mut_type_rows))) {
      pc <- mut_type_rows$aaChange[j]
      vc <- mut_type_rows$Variant_Classification[j]

      if (is.na(pc) || pc == "" || pc == ".") {
        # No protein change available, classify by variant type
        if (vc %in% truncating_variants) {
          cls <- list(GOF = 0L, LOF = 1L, DN = 0L, non_DN = 1L, category = "LOF")
        } else {
          cls <- list(GOF = 0L, LOF = 0L, DN = 0L, non_DN = 0L, category = "VUS")
        }
      } else {
        cls <- classify_tp53_mutation(pc, vc, func_annotation, mv_dn_annotation)
      }

      classified_muts[[j]] <- list(
        protein_change = ifelse(is.na(pc) || pc == "" || pc == ".", "[Not Available]", pc),
        variant_classification = vc,
        GOF = cls$GOF, LOF = cls$LOF,
        DN = cls$DN, non_DN = cls$non_DN,
        category = cls$category
      )
    }

    # Priority: GOF > LOF > VUS
    priority_order <- c("GOF" = 1, "LOF" = 2, "VUS" = 3)
    best_idx <- 1
    best_priority <- priority_order[classified_muts[[1]]$category]
    if (is.na(best_priority)) best_priority <- 99

    if (length(classified_muts) > 1) {
      for (k in 2:length(classified_muts)) {
        k_priority <- priority_order[classified_muts[[k]]$category]
        if (is.na(k_priority)) k_priority <- 99
        if (k_priority < best_priority) {
          best_idx <- k
          best_priority <- k_priority
        }
      }
    }

    best <- classified_muts[[best_idx]]

    # Determine hotspot status
    aa_for_hotspot <- gsub("^p\\.", "", best$protein_change)
    is_hotspot <- ifelse(aa_for_hotspot %in% hotspot_list, 1L, 0L)

    sample_results <- rbind(sample_results, data.frame(
      sample_id = sample,
      cancer_type = ds_cancer,
      protein_change = best$protein_change,
      variant_classification = best$variant_classification,
      wt = 0L, mt = 1L,
      LOF = best$LOF, GOF = best$GOF,
      DN = best$DN, non_DN = best$non_DN,
      hotspot = is_hotspot,
      category = best$category,
      stringsAsFactors = FALSE
    ))
  }

  # Summary for this dataset
  n_total <- nrow(sample_results)
  n_wt <- sum(sample_results$wt == 1)
  n_mt <- sum(sample_results$mt == 1)
  n_gof <- sum(sample_results$GOF == 1)
  n_lof <- sum(sample_results$LOF == 1)
  n_dn <- sum(sample_results$DN == 1)
  n_vus <- sum(sample_results$category == "VUS")
  n_hotspot <- sum(sample_results$hotspot == 1)

  cat("  Results: Total=", n_total,
      " TP53wt=", n_wt, "(", round(n_wt/n_total*100, 1), "%)",
      " TP53mut=", n_mt, "(", round(n_mt/n_total*100, 1), "%)",
      " GOF=", n_gof, " LOF=", n_lof, " DN=", n_dn,
      " VUS=", n_vus, " Hotspot=", n_hotspot, "\n\n")

  # Save individual dataset CSV
  output_file <- file.path(output_dir,
                           paste0(ds_folder, "_TP53_classification.csv"))
  write.csv(sample_results, output_file, row.names = FALSE)

  all_results[[ds_folder]] <- sample_results
}

