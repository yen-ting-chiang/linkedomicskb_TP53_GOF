# ==============================================================================
# Section 3: Classification Functions
# ==============================================================================

#' Classify a single TP53 mutation
#'
#' @param protein_change aaChange from MAF (e.g., "p.R175H")
#' @param variant_class Variant_Classification from MAF
#' @param func_anno Data frame with FunctionDownload annotations
#' @param mv_dn Data frame with MutationView DN annotations
#' @return Named list with GOF, LOF, DN, non_DN, category
classify_tp53_mutation <- function(protein_change, variant_class,
                                   func_anno, mv_dn) {
  result <- list(GOF = 0L, LOF = 0L, DN = 0L, non_DN = 0L, category = "VUS")

  # Extract AAchange from aaChange (remove "p." prefix)
  aa_change <- gsub("^p\\.", "", protein_change)

  # Handle frameshift notation: e.g., "E204Sfs*43" -> look up without fs part
  # These are truncating and should be LOF anyway
  is_truncating <- variant_class %in% truncating_variants || grepl("\\*|fs", protein_change, ignore.case = TRUE)

  # Step 1: Look up in FunctionDownload annotation
  match_idx <- which(func_anno$AAchange == aa_change)

  if (length(match_idx) > 0) {
    row <- func_anno[match_idx[1], ]
    result$GOF <- row$GOF
    result$LOF <- row$LOF
    result$DN  <- row$DN
    result$non_DN <- row$non_DN

    if (result$GOF == 1) {
      result$category <- "GOF"
    } else if (result$LOF == 1) {
      result$category <- "LOF"
    } else {
      # In database but no clear GOF or LOF evidence
      # If truncating -> LOF; else VUS
      if (is_truncating) {
        result$LOF <- 1L
        result$category <- "LOF"
        result$non_DN <- 1L
      } else {
        result$category <- "VUS"
      }
    }
  } else {
    # Step 2: Not found in FunctionDownload
    # Check MutationView for DN info
    mv_match <- which(mv_dn$AAchange == aa_change)
    if (length(mv_match) > 0 && mv_dn$mv_has_DN[mv_match[1]]) {
      result$DN <- 1L
      result$non_DN <- 0L
    }

    if (is_truncating) {
      # Truncating mutations -> LOF
      result$LOF <- 1L
      result$category <- "LOF"
      result$DN <- 0L
      result$non_DN <- 1L
    } else {
      # Non-truncating, not in database -> VUS
      result$category <- "VUS"
    }
  }

  # Ensure DN/non_DN consistency for TP53mut
  if (result$GOF == 1 || result$LOF == 1) {
    if (result$DN == 0) {
      result$non_DN <- 1L
    }
  }

  return(result)
}

